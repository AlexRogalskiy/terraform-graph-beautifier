package main

import (
	"flag"
	"fmt"
	"github.com/pcasteran/terraform-graph-beautifier/cytoscape"
	"github.com/pcasteran/terraform-graph-beautifier/graphviz"
	"github.com/rs/zerolog"
	"github.com/rs/zerolog/log"
	"os"
	"path/filepath"
)

const (
	outputTypeCytoscapeJSON = "cyto-json"
	outputTypeCytoscapeHTML = "cyto-html"
	outputTypeGraphivz      = "graphviz"
)

func getWorkingDir() string {
	dir, err := os.Getwd()
	if err != nil {
		panic(err)
	}
	return filepath.Base(dir)
}

func main() {
	// Prepare command line options.
	inputFilePath := flag.String("input", "", "Path of the input Graphviz file to read, if not set 'stdin' is used")
	outputType := flag.String("output-type", "", fmt.Sprintf("Type of output, can be one the following : %s (default), %s, %s", outputTypeCytoscapeJSON, outputTypeCytoscapeHTML, outputTypeGraphivz))
	outputFilePath := flag.String("output", "", "Path of the output Graphviz file to write, if not set 'stdout' is used")
	debug := flag.Bool("debug", false, "Prints debugging information to stderr")
	// Input reading options.
	var excludePatterns arrayFlags
	flag.Var(&excludePatterns, "exclude", "Pattern (regexp) of the resource to filter out (can be repeated multiple times)")
	keepTfJunk := flag.Bool("keep-tf-junk", false, "Do not remove the \"junk\" nodes and edges generated by 'terraform graph' (default false)")
	// Output writing options.
	graphName := flag.String("graph-name", "", "Name of the Graphviz graph to create (default working directory name)")
	embedModules := flag.Bool("embed-modules", false, "Embed a module sub-graph inside its parent; can lead to really huge graphs (default false)")

	// Parse command line arguments.
	flag.Parse()
	if *graphName == "" {
		cwd := getWorkingDir()
		graphName = &cwd
	}

	// Configure logging.
	// Default level for this example is info, unless debug flag is present.
	zerolog.SetGlobalLevel(zerolog.InfoLevel)
	if *debug {
		zerolog.SetGlobalLevel(zerolog.DebugLevel)
	}
	log.Logger = log.Output(zerolog.ConsoleWriter{Out: os.Stderr})

	// Load the graph from the specified input.
	graph := graphviz.LoadGraph(*inputFilePath, *keepTfJunk, excludePatterns)

	// Write the result to the specified output.
	outputFile := os.Stdout
	var err error
	if *outputFilePath != "" {
		// Write to the specified file.
		outputFile, err = os.Create(*outputFilePath)
		if err != nil {
			log.Fatal().Err(err).Msg("Cannot open the specified file for writing")
		}
		defer func() {
			if err := outputFile.Close(); err != nil {
				log.Fatal().Err(err).Msg("Cannot close the specified after writing")
			}
		}()
	}

	switch *outputType {
	case "", outputTypeCytoscapeJSON:
		log.Debug().Msg("Output graph data to Cytoscape.js JSON format")
		cytoscape.WriteGraphJSON(outputFile, graph)
	case outputTypeCytoscapeHTML:
		// TODO : param template in the log
		log.Debug().Msg("Output graph to HTML")
		cytoscape.WriteGraphHTML(outputFile, graph, &cytoscape.RenderingOptions{
			GraphName:    *graphName,
			EmbedModules: *embedModules,
		})
	case outputTypeGraphivz:
		log.Debug().Msg("Output graph data to Graphviz Dot format")
		graphviz.WriteGraph(outputFile, graph, &graphviz.RenderingOptions{
			GraphName:    *graphName,
			EmbedModules: *embedModules,
		})
	default:
		log.Fatal().Err(err).Msg(fmt.Sprintf("Invalid output type : %s", *outputType))
	}
}
